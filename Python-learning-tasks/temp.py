# testing built-in functions in Python

# finding the minimum value
val1 = min(10, 20, 30, 384, 86, 234, 110, 3609)

print('The smallest number found is:', val1)


# finding the maximum value
val2 = max(10, 20, 30, 384, 86, 234, 110, 3609)

print('The largest number found is:', val2)


# rounding a number to the nearest integer
val3 = round(384.765)

print('Rounded value is:', val3)


# converting a string to an integer
val4 = int('123')

print('Converted value is:', val4)


# converting a string to a float
val5 = float('3.14')

print('Converted value is:', val5)

